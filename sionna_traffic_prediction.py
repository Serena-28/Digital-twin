def nmse_metric(y_true, y_pred):
    mse = tf.reduce_mean(tf.square(y_true - y_pred))
    power = tf.reduce_mean(tf.square(y_true)) + 1e-12
    return mse / power


def nmse_numpy(y_true, y_pred):
    mse = np.mean((y_true - y_pred) ** 2)
    power = np.mean(y_true ** 2) + 1e-12
    return mse / power

