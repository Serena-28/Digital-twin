SEED = 7
rng = np.random.default_rng(SEED)

n_ue = 4
ue_xy = rng.uniform(low=[-8.0, -6.0], high=[8.0, 6.0], size=(n_ue, 2))
ue_v = rng.uniform(low=-1.0, high=1.0, size=(n_ue, 2))
ue_z = 1.2

ues = []
for u in range(n_ue):
    rx = Receiver(name=f"ue{u}", position=[float(ue_xy[u, 0]), float(ue_xy[u, 1]), ue_z])
    scene.add(rx)
    ues.append(rx)

p_solver = PathSolver()

# -----------------------
# 3) Traffic + scheduler + abstract link
# -----------------------
n_slots = 1000
dt = 1e-3  # 1 ms

K_RT = 20          # RT update every 20 slots (faster than 10)
max_depth = 3      # shallower reflections (faster)

lambda_pkt_s = 50.0
pkt_size_bits = 12000

total_ru = 100

# More forgiving MCS so it's easier to get non-zero capacity
MCS_TABLE = [
    (-10.0, 0.10),
    (-6.0,  0.20),
    (-2.0,  0.45),
    ( 2.0,  0.80),
    ( 6.0,  1.30),
    (10.0,  2.00),
    (14.0,  3.00),
]

def select_se(sinr_db):
    se = np.zeros_like(sinr_db, dtype=float)
    for thr, eff in MCS_TABLE:
        se = np.where(sinr_db >= thr, eff, se)
    return se

def se_to_thr_db(se):
    thr = np.full_like(se, -20.0, dtype=float)
    for thr_db, eff in MCS_TABLE:
        thr = np.where(se >= eff, thr_db, thr)
    return thr

def bler_from_margin(sinr_db, thr_db):
    margin = sinr_db - thr_db
    bler = 1.0 / (1.0 + np.exp(1.0 * margin))
    return np.clip(bler, 1e-4, 0.95)

def reflect_bounds(pos, vel, xlim=8.5, ylim=6.5):
    for i in range(pos.shape[0]):
        if pos[i, 0] < -xlim:
            pos[i, 0] = -xlim + (-xlim - pos[i, 0]); vel[i, 0] *= -1
        if pos[i, 0] > xlim:
            pos[i, 0] = xlim - (pos[i, 0] - xlim); vel[i, 0] *= -1
        if pos[i, 1] < -ylim:
            pos[i, 1] = -ylim + (-ylim - pos[i, 1]); vel[i, 1] *= -1
        if pos[i, 1] > ylim:
            pos[i, 1] = ylim - (pos[i, 1] - ylim); vel[i, 1] *= -1
    return pos, vel

# Noise power (dBm): -174 dBm/Hz + 10log10(B) + NF
bandwidth_hz = float(np.array(scene.bandwidth.numpy()).reshape(-1)[0])
noise_fig_db = 7.0
noise_dbm = -174.0 + 10.0*np.log10(bandwidth_hz) + noise_fig_db

pkt_queues = [[] for _ in range(n_ue)]
queue_bits = np.zeros(n_ue, dtype=int)
pkt_id = 0

slot_rows = []
pkt_rows = []

sinr_db_cache = np.full(n_ue, -20.0, dtype=float)

def rt_sinr_from_pathloss(paths, tx_dbm, noise_dbm, n_ue):
    # Prefer using paths.path_loss (dB). If absent, fallback to very safe default.
    if hasattr(paths, "path_loss"):
        pl = paths.path_loss
        pl_np = np.array(pl.numpy()).reshape(-1)
        # pick first n_ue values (rx dimension) robustly
        if pl_np.size >= n_ue:
            pl_db = pl_np[:n_ue]
        else:
            pl_db = np.pad(pl_np, (0, n_ue - pl_np.size), constant_values=200.0)
        rx_dbm = tx_dbm - pl_db
        return rx_dbm - noise_dbm

    # Fallback: if no pathloss available, avoid "all zero throughput" by returning moderate SINR
    return np.full(n_ue, 5.0, dtype=float)

for s in range(n_slots):
    t = s * dt

    # mobility
    ue_xy = ue_xy + ue_v * dt
    ue_xy, ue_v = reflect_bounds(ue_xy, ue_v)
    for u in range(n_ue):
        ues[u].position = [float(ue_xy[u, 0]), float(ue_xy[u, 1]), float(ue_z)]

    # arrivals
    new_pkts = rng.poisson(lam=lambda_pkt_s * dt, size=n_ue)
    arrival_bits = new_pkts * pkt_size_bits
    for u in range(n_ue):
        for _ in range(int(new_pkts[u])):
            pkt_queues[u].append({"pkt_id": pkt_id, "arrival_t": t, "first_tx_t": None, "acked_t": None, "retx": 0})
            pkt_id += 1
    queue_bits += arrival_bits

    # RT update
    if (s % K_RT) == 0:
        paths = p_solver(scene=scene, max_depth=max_depth, synthetic_array=False)
        tx_dbm = float(np.array(bs.power_dbm.numpy()).reshape(-1)[0]) if hasattr(bs.power_dbm, "numpy") else float(bs.power_dbm)
        sinr_db_cache = rt_sinr_from_pathloss(paths, tx_dbm, noise_dbm, n_ue)

    sinr_db = sinr_db_cache.copy()

    # scheduler: allocate all to one active UE (RR)
    active = np.where(queue_bits > 0)[0]
    alloc_ru = np.zeros(n_ue, dtype=int)
    if active.size > 0:
        chosen = int(active[s % active.size])
        alloc_ru[chosen] = total_ru

    # capacity
    se = select_se(sinr_db)
    thr_db = se_to_thr_db(se)
    bler = bler_from_margin(sinr_db, thr_db)

    bw_alloc = (alloc_ru / total_ru) * bandwidth_hz
    cap_bits = (se * bw_alloc * dt).astype(int)
    tx_bits = np.minimum(queue_bits, cap_bits)

    success = np.zeros(n_ue, dtype=bool)
    for u in range(n_ue):
        if tx_bits[u] > 0:
            success[u] = (rng.random() > bler[u])

    ack_bits = np.where(success, tx_bits, 0)
    queue_bits = queue_bits - ack_bits

    # packet-level
    for u in range(n_ue):
        if tx_bits[u] > 0 and (not success[u]) and len(pkt_queues[u]) > 0:
            pkt_queues[u][0]["retx"] += 1

        if ack_bits[u] <= 0:
            continue

        remaining = int(ack_bits[u])
        while remaining > 0 and len(pkt_queues[u]) > 0:
            pkt = pkt_queues[u][0]
            if pkt["first_tx_t"] is None:
                pkt["first_tx_t"] = t
            if remaining >= pkt_size_bits:
                pkt["acked_t"] = t
                pkt_rows.append({
                    "pkt_id": pkt["pkt_id"],
                    "ue_id": u,
                    "arrival_t_s": pkt["arrival_t"],
                    "first_tx_t_s": pkt["first_tx_t"],
                    "acked_t_s": pkt["acked_t"],
                    "delay_s": pkt["acked_t"] - pkt["arrival_t"],
                    "retx": pkt["retx"],
                    "pkt_size_bits": pkt_size_bits,
                })
                pkt_queues[u].pop(0)
                remaining -= pkt_size_bits
            else:
                break

    # slot-level log
    for u in range(n_ue):
        slot_rows.append({
            "slot": s,
            "t_s": t,
            "ue_id": u,
            "ue_x_m": float(ue_xy[u, 0]),
            "ue_y_m": float(ue_xy[u, 1]),
            "ue_z_m": float(ue_z),
            "sinr_db": float(sinr_db[u]),
            "spectral_eff_bphz": float(se[u]),
            "alloc_ru": int(alloc_ru[u]),
            "arrival_bits": int(arrival_bits[u]),
            "tx_bits": int(tx_bits[u]),
            "ack_bits": int(ack_bits[u]),
            "queue_bits": int(queue_bits[u]),
            "bler": float(bler[u]) if tx_bits[u] > 0 else np.nan,
            "success": bool(success[u]) if tx_bits[u] > 0 else None,
        })

# write CSV (packet CSV has headers even if empty)
slot_df = pd.DataFrame(slot_rows)

pkt_cols = ["pkt_id","ue_id","arrival_t_s","first_tx_t_s","acked_t_s","delay_s","retx","pkt_size_bits"]
pkt_df = pd.DataFrame(pkt_rows, columns=pkt_cols)

