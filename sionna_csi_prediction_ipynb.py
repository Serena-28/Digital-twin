class CSIPredictor(nn.Module):
    def __init__(self, d_in, d_out):
        super().__init__()
        # Define a basic Multi-Layer Perceptron (MLP) architecture
        self.net = nn.Sequential(
            nn.Linear(d_in, 128),  # Input layer to 1st hidden layer
            nn.ReLU(),             # Non-linear activation
            nn.Linear(128, 128),   # 2nd hidden layer
            nn.ReLU(),             # Non-linear activation
            nn.Linear(128, d_out), # Output layer for regression
        )

    def forward(self, x):
        # Define the data flow through the network
        return self.net(x)

