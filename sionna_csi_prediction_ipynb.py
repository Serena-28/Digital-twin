def paths_to_csi(paths, freqs):
    # Extract a / tau and handle the tuple case
    a_full = paths.a
    tau_full = paths.tau

    if isinstance(a_full, (tuple, list)):
        a_full = a_full[0]

    if isinstance(tau_full, (tuple, list)):
        tau_full = tau_full[0]

    # Squeeze all dimensions with size=1
    a = tf.squeeze(a_full)
    tau = tf.squeeze(tau_full)

    # Ensure it is 1D [num_paths]
    if tf.rank(a) == 0:
        a = tf.expand_dims(a, 0)
    if tf.rank(tau) == 0:
        tau = tf.expand_dims(tau, 0)

    a = tf.cast(a, tf.complex64)
    tau = tf.cast(tau, tf.float32)
    freqs = tf.cast(freqs, tf.float32)   # [K]

    two_pi = tf.constant(2.0 * np.pi, dtype=tf.float32)
    phase = -two_pi * tau[:, None] * freqs[None, :]
    phase = tf.cast(phase, tf.complex64)

    H = tf.reduce_sum(a[:, None] * tf.exp(1j * phase), axis=0)  # [K]
    return H

